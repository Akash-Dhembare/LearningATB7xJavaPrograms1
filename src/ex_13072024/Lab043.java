package ex_13072024;

public class Lab043 {
    public static void main(String[] args) {
        //Relational Operators -> boolean
        // >, <, >=, <=, ==, != - true or false
        int age_akash = 24;
        int age_pramod = 24;

        boolean result = age_pramod > age_akash;
        boolean result2 = age_pramod < age_akash;
        boolean result3 = age_pramod >= age_akash; //OR gate

        System.out.println(result);
        System.out.println(result2);
        System.out.println(result3);
    }
}
